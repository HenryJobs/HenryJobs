import { Request, Response } from "express";
import { Review, reviewsModel } from "../../models/Reviews";

export const getReviews = async (req: Request, res: Response) => {
  const { idUser } = req.params;
  const { date, score } = req.query;
  try {
    if (!idUser) {
      res.status(404).send("not id user found");
    } else {
      //perdón por usar any
      interface Sort {
        [key: string]: any;
      }
      const obj: Sort = {};
      if (date) {
        obj.date = date;
      }
      if (score) {
        obj.score = score;
      }
      const reviews = await reviewsModel
        .find({ reviewReceiver: idUser })
        .populate("reviewer", "userName")
        .populate("reviewReceiver", "userName")
        .sort(obj);
      if (reviews.length < 1) {
        res.status(404).send("not reviews found");
      } else {
        res.send(reviews);
      }
    }
  } catch (error) {}
};
